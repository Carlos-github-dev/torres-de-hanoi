package colas;

public class Nodo {
    private char data;
    private Nodo next;
    

    public Nodo() {
    }
    
    public Nodo(char dato) {
        this.data = data;
    }

    public char getData() {
        return data;
    }

    public void setData(char data) {
        this.data = data;
    }


    public Nodo getNext() {
        return next;
    }

    public void setNext(Nodo next) {
        this.next = next;
    }
    
    
}
